<p>El Paper Prototype consiste en dibujar en un papel, sin entrar en grandes detalles estéticos, las interfaces que se van a probar y valorar. Los diferentes estados de la interfaz se van dibujando en hojas separadas y mediante un proceso de ordenación que el diseñador determina, permite que el usuario final interactúe con este material simulando el funcionamiento del sistema.<p></p>

Al realizar un prototipo de papel no debemos olvidar que de lo que se trata es de reflejar aquellos aspectos de la interfaz del usuario referidos a las interacciones que el sistema le proporciona. Esto indica un prototipo de papel que deberá mostrar dos aspectos muy importantes:<p>

