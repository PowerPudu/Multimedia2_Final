             <h1>Paper Prototype</h1>

     <p class="lead">Esta técnica de prototipado se basa en la utilización de materiales tan básicos como lápiz, el papel y las tijeras para la creación de prototipos simples pero enormemente versátiles, permite una gran velocidad y flexibilidad en el momento de hacer los prototipos, a la vez que al requerir materiales tan básicos se trata de una técnica “muy económica”.<p>

El objetivo de un prototipo de papel no es probar o verificar lo bonito que es el diseño, sino que se trata de verificar si los usuarios son capaces de realizar sus tareas con la interfaz propuesta. La utilización de esta técnica de prototipado no precisa incorporar avances tecnológicos; sólo es necesario que capture la funcionalidad del sistema y que comunique la información y sus interacciones adecuadamente.
</p>
<h2>Ventajas</h2>
<p>
     <ul class="list-unstyled"><li>Los problemas (funcionales y de usabilidad) se pueden descubrir en una etapa muy temprana del proceso de diseño, mucho antes de haberlos codificado.</li>
    
<li>Favorece la comunicación entre el equipo de diseño-desarrollo, los usuarios y los implicados.</li>
    
<li>Favorece también la participación de todos los miembros de los equipos multidisciplinarios proporcionando un soporte comunicativo entre las diferentes disciplinas.</li>
    
<li>Son muy rápidos de construir y refinar, lo que permite realizar rá interacciones de diseño.</li>
    
<li>Los recursos consumidos son mínimos (materiales muy básicos) y económicos.</li>
    
<li>Psicológicamente es beneficioso para los usuarios.</li>
    
<li>Resulta tan familiar para los usuarios que sin dudarlo intervienen en las modificaciones del diseño.</li>
    
<li>El usuario, que es consciente de la facilidad y el bajo coste de prototipo, no se siente cohibido de proponer cualquier cambio.</li>
    
<li>Resulta menos intimidante que un ordenador (ayuda a superar el fenómeno conocido como tecnofobia).</li>
    
<li>El tiempo dedicado al proceso de codificación es cero.</li>
    
<li>No están sujetos a restricciones impuestas por la tecnología -arquitectura del sistema, la base de datos, el ancho de banda, el sistema operativo-, y a pesar de ello ayuda al equipo a anticipar problemas y decisiones derivadas de la tecnología.</li>
    
         <li>Puede servir como herramienta de marketing y para demostraciones de ventas.</li></ul>
