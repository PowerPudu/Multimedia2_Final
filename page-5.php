             <h1>Diagrama de Flujo</h1>

     <p class="lead">Los diagramas de flujo son una manera de representar visualmente el flujo de datos a través de
sistemas de tratamiento de información, describen que operaciones y en que
secuencia se requieren para solucionar un problema dado. Los diagramas de flujo ayudan a documentar y analizar un proceso, comunicar un proceso para capacitar a otros sectores de la organización o lograr el entendimiento de su parte, identificar cuellos de botellas, redundancias y pasos innecesarios en un proceso y mejorarlo.</p>
                
<h2>Ventajas</h2>
<p>Un buen diagrama de flujo reemplaza varias páginas de texto, su uso adecuado ayuda a resolver problemas de forma sencilla, respondiendo a preguntas clave de un problema. </p>

                
 <p><a class="btn btn-lg" href="img/diagrama/ejemplo_diagrama.jpg" target="_blank">Descargar Ejemplo</a>  creado por Lucid Software Inc.</p>
                  
                                    

<p>Para construir adecuadamente un diagrama de flujo debemos seguir una serie de reglas, usando algunos símbolos estándares; sin embargo, algunos símbolos especiales pueden también ser desarrollados cuando séan requeridos. Algunos símbolos estándares, que se requieren con frecuencia para diagramar programas de computadora se muestran a continuación:<p>
 <ul class="list-unstyled">
    <li>Determinar el propósito o función del diagrama de flujo.
</li>
    <li>Agregar pasos y conectalos con flechas.
</li>
    <li>Agregar decisiones o dividir caminos
</li>
    <li>Mostrar cualquier bucle de vuelta a los pasos anteriores.
</li>
     <li>¡Compartir tu diagrama de flujo!.</li></ul>
    </p>
</p></p>
